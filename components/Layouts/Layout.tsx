import React, { ReactNode, useState } from 'react';
import Head from 'next/head';
import { useRouter } from 'next/router';
import { Toaster } from 'react-hot-toast';
import Footer from '../Navigation/Footer';
import Header from '../Navigation/Header/Header';
import Image from 'next/image';
import styles from '../../styles/Layouts.module.scss';
import ModalCart from '../Modals/ModalsComponents/ModalCart';
import ModalMenu from '../Modals/ModalsComponents/ModalMenu';

interface Props {
    children: ReactNode;
    title: string;
    banner?: string; // AquÃ­ recibimos el banner desde las props
}

export const Layout = ({ children, title, banner }: Props) => {
    const [openModalCart, setOpenModalCart] = useState(false);
    const [openModalMenu, setOpenModalMenu] = useState(false);
    const { pathname } = useRouter();

    return (
        <>
            <Head>
                <title>{title}</title>
                <meta name="description" content="Generated by create next app" />
                <meta name="viewport" content="width=device-width, initial-scale=1" />
                <link rel="icon" href="/circle-solid.svg" />
            </Head>

            <Header setOpenModalCart={setOpenModalCart} setOpenModalMenu={setOpenModalMenu} />

            <div className={styles.Layout}>
                {
                    pathname === '/products' &&
                    <div className={styles.banner}>
                        <Image
                            fill
                            alt="Banner"
                            src={banner ?? '/Banner_olei.png'}
                            quality={75}
                            loading="lazy"
                        />
                    </div>
                }

                <div>{children}</div>
            </div>

            {pathname !== "/cart" && <Footer />}

            <ModalCart
                visible={openModalCart}
                onClose={() => setOpenModalCart(false)}
            />

            <ModalMenu
                visible={openModalMenu}
                onClose={() => setOpenModalMenu(false)}
            />

            <Toaster />
        </>
    );
};

