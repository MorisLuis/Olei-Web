import React, { ReactNode, useState } from 'react';

import Head from 'next/head';
import { useRouter } from 'next/router';
import { Toaster } from 'react-hot-toast';
import ModalCart from '../Modals/ModalCart';
import Footer from '../Navigation/Footer';
import Header from '../Navigation/Header/Header';
import ModalMenu from '../Modals/ModalMenu';
import Image from 'next/image';

interface Props {
    children: ReactNode
}

export const Layout = ({ children }: Props) => {

    const [openModalCart, setOpenModalCart] = useState(false)
    const [openModalMenu, setOpenModalMenu] = useState(false)
    const { pathname } = useRouter()


    return (
        <>
            <Head>
                <title>Olei web</title>
                <meta name="description" content="Generated by create next app" />
                <meta name="viewport" content="width=device-width, initial-scale=1" />
                <link rel="icon" href="/circle-solid.svg" />
            </Head>

            <Header setOpenModalCart={setOpenModalCart} setOpenModalMenu={setOpenModalMenu} />

            <div className='banner'>
                <Image
                    fill
                    alt="banner"
                    src={'/bannerMario.jpg'}
                    //src={'https://m.media-amazon.com/images/S/al-na-9d5791cf-3faf/5a6f9d5c-c80d-4e1f-ad48-08a13d7be695._CR0%2C0%2C3000%2C600_SX3000_.jpg'}
                />
            </div>

            <div>
                {children}
            </div>

            {
                pathname !== "/cart" &&
                <Footer />
            }

            <ModalCart
                visible={openModalCart}
                onClose={() => setOpenModalCart(false)}
            />

            <ModalMenu
                visible={openModalMenu}
                onClose={() => setOpenModalMenu(false)}
            />

            <Toaster />
        </>
    )
}
