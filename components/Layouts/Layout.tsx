import Head from 'next/head'
import { useRouter } from 'next/router'
import React, { useState } from 'react'
import ModalCart from '../Modals/ModalCart'
import Footer from '../Navigation/Footer'
import Header from '../Navigation/Header'

interface Props {
    children: any,
    filterActive?: any,
    setFilterActive?: any
}

export const Layout = ({ children, filterActive, setFilterActive}: Props) => {

    const [openModalCart, setOpenModalCart] = useState(false)
    const { pathname } = useRouter()

    return (
        <>
            <Head>
                <title>Olei web</title>
                <meta name="description" content="Generated by create next app" />
                <meta name="viewport" content="width=device-width, initial-scale=1" />
                <link rel="icon" href="/circle-solid.svg" />
            </Head>

            <Header
                setOpenModalCart={setOpenModalCart}
                filterActive={filterActive}
                setFilterActive={setFilterActive}
            />

            <div>
                {children}
            </div>

            {
                pathname !== "/cart" &&
                <Footer />
            }


            <ModalCart
                visible={openModalCart}
                onClose={() => setOpenModalCart(false)}
            />
        </>
    )
}
